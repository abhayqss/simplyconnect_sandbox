  id           bigint not null,
  code         varchar(25),
  display_name varchar(max)
);

with source as
(
    select
      p.code,
      p.title
    FROM @procedureCodes p left join ConcreteCcdCode c on p.code = c.code and c.code_system = '2.16.840.1.113883.6.96'
    where c.id is null
)
merge into AnyCcdCode as target
using source
on (1 <> 1)
when not matched then INSERT DEFAULT VALUES
