    public static Permission getRole(CareTeamRoleCode code) {
        return rolePermissionMapping.getOrDefault(code, Permission.ROLE_UNKNOWN);
    }

    public static Set<Permission> getPermissions(CareTeamRoleCode code) {
        return permissionsMap.getOrDefault(code, Set.of());
    }

    public static boolean roleHasPermission(CareTeamRoleCode code, Permission permission) {
        return permissionsMap.getOrDefault(code, Set.of()).contains(permission);
    }

    public static Set<CareTeamRoleCode> findCareTeamRoleCodesWithPermission(Permission permission) {
        return permissionsMap.entrySet()
                .stream()
                .filter(entry -> entry.getValue().contains(permission))
                .map(Map.Entry::getKey)
                .collect(Collectors.toSet());
    }

    public static Set<CareTeamRoleCode> findCareTeamRoleCodesWithAnyPermission(Collection<Permission> permissions) {
        return permissionsMap.entrySet().stream()
                .filter(entry -> entry.getValue().stream().anyMatch(permissions::contains))
                .map(Map.Entry::getKey)
                .collect(Collectors.toSet());
    }
